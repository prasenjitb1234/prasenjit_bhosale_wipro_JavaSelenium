package day_3_assignment.variables;

public class SimpleInterest {
	// calculate simple interest
	public static void main(String[] args) {
		
		
		float p = 20000.0f;
		float r = 5.2f;
		float n = 2.3f;
		
		// simple interest 
		
		float si = (p * r * n ) / 100;
		
		System.out.println("Simple Interest is : "+si);
		
		
	}
}
